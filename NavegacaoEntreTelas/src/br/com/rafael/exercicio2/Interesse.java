package br.com.rafael.exercicio2;

public class Interesse {

	private Integer id;
	private String area;

	public Interesse() {
		super();
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

}
